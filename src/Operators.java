public class Operators {
    public static void  main(String[] args){
        int a=10, b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);

        System.out.println(a > 5 && b < 5);
        System.out.println(a > 5 || b < 5);
        System.out.println(a+=5);
        System.out.println(a-=5);
        System.out.println(a*=5);
        System.out.println(a/=5);

        System.out.println(a);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(a);
    }
}
